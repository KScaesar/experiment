package main

import (
	"github.com/gin-gonic/gin"
	swaggerFiles "github.com/swaggo/files"
	ginSwagger "github.com/swaggo/gin-swagger"

	"experiment/swag/api"
	_ "experiment/swag/docs" // docs is generated by Swag CLI, you have to import it.
)

//go:generate swag init -g ./main.go -o ../docs --parseDependency

// @title Swagger Example API
// @version 1.0
// @contact.name caesar API Support
// @contact.email support@caesar.io
// @host localhost:8080
// @BasePath /
func main() {
	r := gin.New()

	r.GET("/swagger/*any", ginSwagger.WrapHandler(swaggerFiles.Handler))

	r.GET("user", api.GetUser)
	r.POST("user", api.CreateUser)

	r.Run(":8080")
}
